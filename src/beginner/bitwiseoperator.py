#Generate codes for bitwise operator
a=20
b=10
print(f"Bitwise AND (a & b): {a & b}")  # Output: 0
print(f"Bitwise OR (a | b): {a | b}")   # Output: 30
print(f"Bitwise XOR (a ^ b): {a ^ b}")  # Output: 30
#~x = -(x + 1), so ~20 = -(20 + 1) = -21
print(f"Bitwise NOT (~a): {~a}")
# If x is a number, then x << n is x multiplied by 2 to the power of n
print(f"Left Shift (a << 2): {a << 2}")  
print(f"Right Shift (a >> 2): {a >> 2}") # Output: 5

